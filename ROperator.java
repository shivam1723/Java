public class ROperator {
    // == != > < >= <=
    public static void main(String[] args) {
        int x=10;
        int y=5;
        System.out.println("x is "+x+" y is "+y );
        System.out.println("equals to Operator "+ (x==y));
        System.out.println("Not equls to "+ (x!=y));
        System.out.println("greater than "+(x>y));
        System.out.println("Less than "+ (x<y));
        System.out.println("greater than equals "+ (x>=y));
        System.out.println("Less than equals to "+(x<=y));
    }
    
    
}
